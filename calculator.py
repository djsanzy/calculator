def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}


def calculator():
    num1 = float(input("What's the first number?: \n"))
    still_calculating = True

    while still_calculating:
        for symbol in operations:
            print(symbol)
        operation_choice = input("Pick an operation: \n")
        num2 = float(input("What's the next number?: \n"))
        operation_func = operations[operation_choice]
        answer = operation_func(num1, num2)

        user_choice = input(f"Type 'y' to continue with {answer}, or type 'n' to start a new calculator: \n").lower()

        if user_choice == 'y':
            num1 = answer
        elif user_choice == 'n':
            calculator()
        else:
            print("Wrong Input")
            still_calculating = False


calculator()